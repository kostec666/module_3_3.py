def print_params(a=1, b='строка', c=True):
    """Функция выводит параметры."""
    print(f'a: {a}, b: {b}, c: {c}')

# Вызовы функции
print_params()                         # Вызываем без аргументов
print_params(10)                       # С одним аргументом
print_params(10, 'новая строка')      # С двумя аргументами
print_params(b=25)                     # Аргумент по имени
print_params(c=[1, 2, 3])              # Аргумент по имени

# Создание списка и словаря для распаковки
values_list = [3.14, 'текст', False]
values_dict = {'a': 42, 'b': 'пример', 'c': None}

# Передача списка и словаря в функцию
print_params(*values_list)              # Распаковка списка
print_params(**values_dict)              # Распаковка словаря

# Новый список для дополнительной проверки
values_list_2 = ['строка', 99]

# Вызов функции с распаковкой
print_params(*values_list_2, 42)        # Распаковка и отдельный аргумент